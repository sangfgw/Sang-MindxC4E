export const isPositiveNumber = (num = 0) => num > 0 ? true : false;
export const add = (num1 = 0, num2 =0) => num1 + num2;
export const minus = (num1 = 0, num2 = 0) => num1 - num2;
export const times = (num1 = 0, num2 = 0) => num1 * num2;
export const divide = (num1 = 0, num2 = 0) => num1 / num2;
export const characterCount = (str = "") => `${str} has ${str.length} character(s)`;